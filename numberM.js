//parseInt(): is a function that parses a string argument and returns an integer
console.log("parseInt() function:");
const num = "65";
console.log(num);
console.log(parseInt(num));

//parseFloat(): is a function parses a string argument and returns a floating point number
console.log("parseFloat() function:");
console.log("0.0314E+2");
console.log(parseFloat("0.0314E+2"));

//Number(): converts a string in a number type
console.log("Number() function:");
const numb = "5535.259";
console.log(numb);
console.log(Number(numb));

